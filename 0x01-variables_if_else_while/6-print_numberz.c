#include <stdio.h>

/**
 * main - the program entry point
 * Description: This function prints numbers from 0 to 9
 * Return: the function returns zero
 */
int main(void)
{
        int i;

        for (i = 48; i <= 57; i++)
        {
                putchar(i);
        }
        putchar('\n');

	return (0);
}
